            <h3>¿Qué es PHP?</h3>
            <p>
                PHP es un lenguaje de programación destinado a desarrollar aplicaciones para la web y crear páginas web, favoreciendo la conexión entre los servidores y la interfaz de usuario.
                Entre los factores que hicieron que PHP se volviera tan popular, se destaca el hecho de que es de código abierto.
                Esto significa que cualquiera puede hacer cambios en su estructura.
            <p>
                El PHP generalmente es definido como un lenguaje del lado del servidor. Esto significa que se aplica en la programación que tiene lugar en el servidor web responsable de ejecutar la aplicación o, más a menudo, en un sitio web.
                Este trabajo previo permite cargar los elementos de una página antes de mostrarlos al usuario que accede a un sitio web, por ejemplo.
                El código PHP se ejecuta en el servidor que, al leer los comandos, puede activar todos los elementos funcionales y la interfaz visual del sitio web.
            </p>
        </section>
        <section class="history">
            <h3>Historia de PHP</h3>
            <p>
                PHP es un software libre, nacido en 1994 de la mano de Rasmus Lerdof, que ha ido creciendo gracias a las aportaciones de los miembros de la gran comunidad PHP, que hoy en día cuenta con un potente núcleo de lenguaje y con muchísimas librerías.
                Inicialmente, PHP (Personal Home Page Tools) surgió como un CGI escrito en C y era capaz de interpretar una serie limitada de comandos. Pronto, muchas personas se interesaron por el sistema y solicitaron a su creador autorización para poder usarlo en sus propias páginas. Esto hizo que se conociese rápidamente. Tanto fue así, que Rasmus Lerdof diseñó un sistema específico para procesar e interpretar formularios llamado FI (Form Interpreter), que unido al Personal Home Page Tools (o PHP) dieron lugar a la primera versión compacta del lenguaje, que se denominaría PHP/FI.
                Sería tres años más tarde cuando se añadieron grandes mejoras, como fue la inclusión del analizador sintáctico, el soporte a nuevos protocolos de Internet y a casi todas las bases de datos comerciales, sentando así las bases de lo que sería la versión 3 de PHP. Estas novedades animaron a los programadores a utilizar PHP para desarrollar sus webs, haciendo crecer día a día la comunidad PHP.
            </p>
        </section>
        <section class="especs">
            <h3>Características de PHP</h3>
            <h3>Código abierto</h3>
            <p>
                Como es un lenguaje de código abierto, PHP no genera costos para los programadores que desean trabajar con él, lo cual es una gran ventaja, especialmente para aquellos que trabajan de manera autónoma.
                Esto representa oportunidades para desarrollar aplicaciones generales para la web, sin ningún problema legal con los fundadores del lenguaje.
                Los programadores también pueden usar el facilitador de código abierto para mejorar aún más PHP, eliminando cualquier tipo de fallas, bugs o, incluso, trabajando en la optimización del desempeño.
            </p>
            <h3>Admite una gran cantidad de datos</h3>
            <p>
                Una de las principales preocupaciones de los programadores es la complejidad que tendrán sus aplicaciones web, ya que cuantos más recursos tengan, más datos comenzarán a administrar.
                Los sitios con una variedad de detalles visuales, ecommerces y otros proyectos web de gran porte tienden a necesitar una gran cantidad de información cuando están activos.
                Sin PHP, sería difícil poder ejecutar estas aplicaciones con el rendimiento mínimo requerido para la experiencia de navegación.
            </p>
            <h3>Compatibilidad con las principales bases de datos</h3>
            <p>
                El lenguaje PHP vuelve todo más simple, debido a que resuelve bien estos dos puntos, sin restringir el rendimiento de la aplicación.
                Entre las principales bases de datos utilizadas, PHP es compatible con:
                <li>Oracle;</li>
                <li>MySQL;</li>
                <li>Interbase;</li>
                <li>SQLite;</li>
                <li>Sybase.</li>
            </p>

